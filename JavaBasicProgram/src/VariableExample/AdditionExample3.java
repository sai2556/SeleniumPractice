package VariableExample;

public class AdditionExample3 {

	public static void main(String[] args) {
		System.out.println("program start");
		int num1=50,num2=60,result;
		result=num1+num2;
		System.out.println("number 1 is :"+num1);
		System.out.println("number 2 is :"+num2);
		System.out.println("addition of tow number is:"+result);
		num1=40;
		num2=20;
		result=num1+num2;
		System.out.println("number 1 is :"+num1);
		System.out.println("number 2 is :"+num2);
		System.out.println("addition of tow numbers is:"+result);
		System.out.println("program ends");
		

	}

}
